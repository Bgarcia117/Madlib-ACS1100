# Use input to collect each word to a variable
# Use an f string to display the story

# Your madlib must collect at least 6 words!

name = input("Hi, what's your name?")
greeting = input(f"Hi {name}, how are you doing today?")
print(f"You're doing {greeting}? I am too, thanks for being considerate and asking.")
hobbies = input(f"What would you say your favorite hobby is {name}?")
print("That sounds kinda boring, but it's a hobby I guess.")
food = input("What's your favorite food anyways?")
print(f"{food}? Those are okay, but not as good as oreos")
school = input(f"What school do you go to {name}?")
print(f"{school}? Me too!")
location = input("One last thing, where are you from?")
print("Woah that's far from here")
print(f"Well {name}, your favorite hobby is {hobbies}, your favorite food is {food}, you go to {school}, and you're from {location}. Based on your infomation, I think we can be friends.")



































